	<h1><i class="icon-doc"></i>Сучасний вісник</h1>
	<p> Збірник «Сучасний вісник» ДПТНЗ «Дніпровський центр професійно-технічної освіти» створений для публікації  статей, методичних розробок різних напрямів підготовки. </p>
	<p> У збірниках розглядаються  теоретико – методологічні та методичні наробки модернізації навчального закладу і навчання в сучасних соціально-економічних умовах. Проаналізовано інноваційні підходи до професійної орієнтації молоді на робітничі професії, формування змісту професійної освіти на компетентнісній основі.</p> 
	<p>Рекомендовано для учнів, викладачів, майстрів виробничого навчання ПТНЗ.</p>
	
