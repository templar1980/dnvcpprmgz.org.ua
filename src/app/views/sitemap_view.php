	$('.main-level').click(function(event) {
		$elem = $(this).next();

		if ($elem.is(':visible')) {
			$elem.slideUp(500);
			$(this).css({width:'50%'});
		} else {
			$elem.slideDown(500);
			$(this).css({width:'100%'});
		};
		
	});

	$('#txt-view').click(function(event) {
		$('.main-level').css({width:'100%'});
		$('.main-level').next().slideDown(10);
		$('.main-level').removeClass('main-level').addClass('txt-level');
		$('button').removeClass('active');
		$(this).addClass('active');
	});

	$('#icon-view').click(function(event) {
		$('.txt-level').removeClass('txt-level').addClass('main-level');
		$('button').removeClass('active');
		$(this).addClass('active');
	});
</script>