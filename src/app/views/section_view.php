	<h1><i class="fa fa-pie-chart"></i>Гурткова робота</h1>
	<img src="/img/pages/_content/section_main.jpg" alt="" style="width: 100%; margin-bottom: 10px">

	<p><strong>Гурткова робота</strong> в нашому центрі є частиною освітнього процесу. Це один із найефективніших шляхів розвитку творчо спрямованої особистості. Саме тому організація роботи гуртків, секцій, студій  потребує особливої уваги з боку адміністрації.</p>         
	<p>Результативність цієї роботи значною мірою залежить від чіткої,  продуманої  її організації, планування та координації. <strong>Мета гурткової роботи</strong> &ndash; це створення умов для творчого, інтелектуального, духовного та фізичного розвитку учнівської молоді у вільний від навчання час, підготовка підлітків до життя в умовах переходу до ринкової економіки при впровадженні якісно нових форм і методів організації  позанавчальної життєдіяльності учнів.</p>

