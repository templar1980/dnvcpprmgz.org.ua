		<p><i class="fa fa-info-circle"></i><strong>Повинен знати:</strong></p> 
		<ul style="list-style:  circle ">
			<li>конструкцію і призначення складових одиниць та агрегатів автомобілів середньої складності</li>
			<li>правила складання автомобілів, способи і прийоми ремонту деталей, складових одиниць, агрегатів, вузлів і приладів автомобілів</li>
			<li>основні прийоми розбирання, складання, знімання та встановлення приладів і агрегатів електрообладнання; типові несправності вузлів, агрегатів, систем автомобілів, способи їх виявлення та усунення</li>
			<li>призначення і основні властивості матеріалів, які застосовуються під час ремонту електрообладнання</li>
			<li>правила і порядок виконання регулювальних та кріпильних робіт</li>
			<li>основні властивості металів</li>
			<li>призначення термообробки деталей; будову універсальних спеціальних пристроїв і контрольно-вимірювальних приладів середньої складності</li>
			<li>поняття про систему допусків і посадок, квалітети та параметри шорсткості</li>
			<li>особливості будови сучасних автомобілів і автобусів, прийоми і методи технічного обслуговування і ремонту автомобілів, призначення та види діагностичного обладнання, стандартизацію та контроль якості</li>
			<li>правила та порядок користування інструментами та приладами для виміру лінійних і кутових величин, оптичних, пневматичних та електричних приладів</li>
			<li>розміри на кресленнях, різні схеми відповідно до спеціалізації, користуватися ними</li>
			<li>метали і сплави, діелектрики, паливно-мастильні матеріали, рідини</li>
			<li>основи електростатики, особливості використання в професійній діяльності постійного струму, магнетизму та електромагнетизму,змінного струму, трансформаторів, електричних машин, апаратури управління і захисту, електровимірювальних приладів</li>
		</ul>
		<p><i class="fa fa-gear"></i><strong>Повинен вміти:</strong></p>
		<ul style="list-style:  circle ">
				<li>виконувати роботи з розбирання автомобілів (у тому числі дизельних, інжекторних і спеціальних), а також автобусів; ремонтувати та обслуговувати вантажні автомобілі, окрім спеціальних, газобалонних та дизельних, легкові автомобілі і автобуси з кількістю посадочних місць для пасажирів до 22</li>
				<li>визначати і усувати несправності у роботі складових одиниць, вузлів та агрегатів автомобілів</li>
				<li>розбирати, ремонтувати, складати, регулювати та випробовувати складові одиниці, вузли та агрегати середньої складності</li>
				<li>виконувати кріпильні роботи відповідальних різьбових з’єднань при технічному обслуговуванні із заміною зношених деталей</li>
				<li>розбирати відповідальні складові одиниці та агрегати електрообладнання автомобілів</li>
				<li>з’єднувати та паяти провідники з приладами, вузлами й агрегатами електрообладнання</li>
				<li>виконувати слюсарну обробку деталей за 11-12 квалітетами із застосуванням універсального обладнання</li>
				<li>розбирати, ремонтувати, складати і встановлювати складні агрегати, вузли і складові одиниці автомобілів під керівництвом слюсаря вищої кваліфікації</li>
		</ul>

