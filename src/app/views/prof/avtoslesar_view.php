	<div class="specialty-view-name"><span>Слюсар з ремонту автомобілів</span></div>
	<div class="specialty-view-description">
		<p><strong>Слюсар з ремонту автомобілів</strong> – спеціаліст, який виконує ремонт і технічне обслуговування автомобільного транспорту, а також здійснює контроль над технічним станом автомобілів за допомогою діагностичного обладнання.</p>
		<p>В автомобільному транспорті професія слюсаря з ремонту автомобілів - це професія №2 після водія, якість і результат роботи якого багато в чому залежить від справності автомобіля. За класифікацією професія слюсаря з ремонту автомобілів відноситься до професій типу "людина - техніка".</p>
		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
		<ul>
			<li><strong>1рік</strong> &ndash; на базі повної загальної середньої освіти (11класів)</li>
			<li><strong>3 роки</strong> &ndash; на базі базової загальної середньої освіти  (9 класів) в інтеграції з професією: <a href="/specialty/voditel">Водій автотранспортних засобів категорії «В» і «С»</a></li>
			<li><strong>4-6 місяців</strong> &ndash; професійно-технічне навчання (перепідготовка та підвищення кваліфікації) за рахунок фізичних або юридичних осіб</li>
		</ul>
		<div class="warning">
			<div><i class="fa fa-info-circle"></i></div>
			<div>
				<p>Навчання проводиться на бюджетній основі, без сертифікатів ЗНО</p>
				<p>Всім бажаючим надається гуртожиток</p>
			</div>
		</div>
		<p>Щоб автомашина запрацювала, її потрібно зібрати, налагодити, відрегулювати і випробувати, у процесі експлуатації - обслуговувати і ремонтувати. Все це - справа слюсаря з ремонту автомобілів. Чим складніша машина, тим вища кваліфікація слюсаря, і якими б не були досконалими інструменти, завжди буде потрібна людина з "розумними" руками. Тому потреба у спеціалістах з ремонту автомобілів буде постійною і, оволодівши цією професією, можна легко працевлаштуватися на різних автопідпрємствах, фірмах автосервісу, автокомбінатах, автопарках, "шляхових допомогах", навіть відкрити свою справу.</p>
		<p>Професія слюсаря з ремонту автомобілів вважається престижною. У майбутньому професія збережеться, але в ній відбудуться суттєві зміни. Деякі операції зникнуть, ручна праця зведеться до мінімуму і процес в цілому буде автоматизований.</p>
