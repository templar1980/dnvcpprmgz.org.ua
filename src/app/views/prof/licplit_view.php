	<div class="specialty-view-name"><span>Лицювальник-плиточник</span></div>
	<div class="specialty-view-description">
		<p><strong>Лицювальник-плиточник</strong> &ndash; одна з найцікавіших будівельних професій, специфіка якої полягає в тому, що лицювальник має справу в основному з внутрішніми поверхнями будинків. Облицьовує плиткою стіни будинків, опори мостів і т.п., а також внутрішні приміщення. Сортує керамічну плитку за розміром, кольором, сортом. Укладає керамічну плитку. Для цього готує розчини, очищує поверхні, ріже плитку за розмірами. Може укладати килимову кераміку за заданомим малюнком. Ремонтує облицьовані поверхні.</p>

		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
		<ul>
			<li><strong>3 роки</strong> &ndash; на базі базової загальної середньої освіти (9 класів) в інтеграції з професіями: <a href="/specialty/malyar">Маляр</a> та <a href="/specialty/stukatur">Штукатур</a></li>
		</ul>
		<div class="warning">
