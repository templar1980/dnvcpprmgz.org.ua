				<li>приготувати розчин і клеючу суміш механізовано;</li>
				<li>укладати гідроізоляцію на різні основи;</li>
				<li>виконувати улаштування стяжки по маркам і маякам та з використанням сучасних матеріалів;</li>
				<li>облицьовувати поверхню стін керамічною плиткою на розчині і з використанням клеючих сумішей;</li>
				<li>настилати основи керамічною плиткою на розчині і з використанням клеючих сумішей;</li>
				<li>заповнювати і оброблювати шви поверхонь плиток;</li>
				<li>перевіряти вертикальність і горизонтальність облицьованих поверхонь; </li>
				<li>ремонтувати облицьовані поверхні та усувати дефекти.</li>
		</ul>

