		<p>Професія лицювальника-плиточника відноситься до комплексу професій оздоблювальних робіт, що виконуються з метою підвищення довговічності будинків, архітектурних та інженерних споруд, надання їм естетичного вигляду. У процесі роботи лицювальник-плиточник застосовує різні механізми та інструменти: мозаїчно-шліфувальні машини, розчиномішалки, трамбівки, катки, скребки, кельми, шпателі, рівні тощо. Виконує роботи середньої складності під час облицьовування керамічними, скляними, азбестовими та іншими плитками.</p>

		<p><i class="fa fa-bullseye"></i><strong>Умови роботи: </strong> Лицювальник-плиточник оздоблює стіни та підлоги природними й синтетичними кахлями, виконує мозаїчні роботи. Прогресивні технології та новітні матеріали стають основою підготовки та формування вмінь молодих спеціалістів, які працюють на будівельних майданчиках нашої України. Будівельник – це покликання, нескінченна творчість і пошук, а плоди його праці переживуть століття. Якщо ви людина творча і усвідомлюєте, що ваша праця необхідна людям – вчиться на лицювальника-плиточника. Лицювальник – плиточник повинен об’єднувати в собі дві протилежні якості з одного боку, високе почуття відповідальності, дисциплінованість, з іншого – творче новаторство, сміливий політ фантазії та розкутість думок.</p>

		<p><i class="fa fa-cogs"></i><strong>Ринок праці: </strong> лицювальник-плиточник може працювати у комерційних та приватних структурах, де заробітна плата визначається контрактом і становить комерційну таємницю. Взагалі професія лицювальника-плиточника добре оплачується і набуває престижності. Будівельні організації України, домобудівні комбінати, ремонтні та експлуатаційні управління міст і сіл відчувають постійну потребу в фахівцях цієї професії. Доволі часто можна побачити об’яви із запрошенням кваліфікованих лицювальників-плиточників на роботу у країнах СНД та за кордоном. Тому вірогідність працевлаштування лицювальника-плиточника після навчання є високою. Ризик безробіття – мінімальний.</p>
		
