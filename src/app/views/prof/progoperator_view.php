	<div class="specialty-view-name"><span>Оператор верстатів з програмним керуванням</span></div>
	
	<div class="specialty-view-description">
		<p><strong>Верстатник</strong> &ndash; одна з найпоширеніших робітничих професій. Обробляючи безформний метал, він одержує складні за формою деталі – вироби високої точності та чистоти обробки. Основний інструмент верстатника – різці різних типів. Кваліфікований верстатник вміє працювати на різних типах металорізальних верстатів (токарних, фрезерних, шліфувальних…). Недарма таких універсалів називають верстатниками широкого профілю або «чарівниками різця». Верстатне ремесло - не просто технічна спеціальність; це свого роду мистецтво. Адже з грубої заготівки цей фахівець робить акуратну деталь з блискучою поверхнею.</p>
		
		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
