		<p>Оператор верстатів з ЧПК забезпечує процес оброблення металевих деталей на верстатах з програмним керуванням. Він веде процес оброблення деталей з пульта керування верстата; установлює і знімає деталі після оброблення; стежить за роботою систем верстатів, які обслуговує, може керувати групою верстатів; контролює оброблення поверхонь деталей, перевіряє якість оброблення деталей контрольно-вимірювальним інструментом та візуально; налагоджує окремі вузли і механізми; контролює вихід інструменту в початкову точку та коректує його; установлює інструмент в інструментальні блоки; підбирає та установлює інструментальні блоки з заміною.</p>

