		<p><i class="fa fa-info-circle"></i><strong>Повинен знати:</strong></p> 
		<ul style="list-style:  circle ">
			<li>принцип роботи верстатів з програмним керуванням, які обслуговує</li>
			<li>правила керування устаткуванням, яке обслуговує</li>
			<li>найменування, призначення, будову та умови застосування найбільш розповсюджених пристроїв, різального, простого і середньої складності контрольно-вимірювального інструменту</li>
			<li>ознаки затуплення різального інструменту, найменування, маркування і основні механічні властивості матеріалів, які обробляє</li>
			<li>основи знань з гідравліки, механіки та електротехніки</li>
			<li>умовну сигналізацію, яка застосовується на робочому місці</li>
			<li>призначення умовних знаків на панелі керування верстатом</li>
			<li>правила установлення перфострічок в лічильний пристрій</li>
			<li>способи повернення програмоносіїв до першого кадру</li>
			<li>основи знань про допуски і посадки, квалітети і параметри шорсткості (класи точності і чистоти оброблення)</li>
			<li>призначення і властивості охолоджувальних і мастильних рідин; читання креслень деталей, які обробляє</li>
		</ul>
		<p><i class="fa fa-gear"></i><strong>Повинен вміти:</strong></p>
		<ul style="list-style:  circle ">
				<li>вести процес оброблення з пульта керування простих деталей за 12-14 квалітетами (5-7-м класами точності) на налагоджених верстатах з програмним керуванням з одним видом оброблення</li>
				<li>установлювати і знімати деталі після оброблення</li>
				<li>стежити за роботою систем верстатів, які обслуговує, за показаннями цифрових табло та сигнальних ламп</li>
				<li>перевіряти якість оброблення деталей контрольно-вимірювальним інструментом та візуально</li>
				<li>підналагоджувати окремі прості і середньої складності вузли і механізми під керівництвом оператора більш високої кваліфікації</li>
		</ul>

