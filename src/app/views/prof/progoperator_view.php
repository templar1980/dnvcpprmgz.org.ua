			<p><i class="fa fa-bullseye"></i><strong>Умови роботи: </strong> оператор верстатів з ЧПК працює в закритому приміщенні, при хорошому освітленні, нормальній температурі повітря. Робота оператора верстатів з ЧПК двозмінна, режим вільний. Тривалість робочого дня – 8 годин, для неповнолітніх – 6. Робота оператора верстатів кваліфікується як праця середньої важкості. Вона добре механізована, виконувати її можуть як чоловіки, так і жінки. Заробітна плата оператора верстатів з ЧПК залежить від кваліфікації і складності виконуваної роботи. На підприємствах, крім цього, діє система матеріального заохочення: премії за виконання виробничих планів, за хорошу якість продукції, за раціоналізацію і винахідництво та інші форми.</p>
