		<p>Верстати з числовим програмним управлінням стали широко застосовуватися в машинобудуванні в кінці п'ятдесятих років. Налаштування, тобто вчення верстата тієї або іншої технологічної операції, вироблялося за допомогою запису на магнітну стрічку програми виготовлення першої (еталонною) деталі. Спочатку такі верстати спеціалізувалися на виконанні однієї групи операцій - фрезерування або свердління, тому значна доля робочого часу оператора витрачалася на передачу деталі з ділянки на ділянку. Для усунення цього недоліку у вітчизняному машинобудуванні були розроблені багатоопераційні верстати з ЧПУ. Автоматизація - це сьогодення і майбутнє розвитку промисловості. Вже зараз на підприємствах працюють токарні, фрезерні. шліфувальні верстати з ЧПУ. Їх обслуговують висококваліфіковані фахівці – оператори.</p>
		
