	<div class="specialty-view-name"><span>Оператор поштового зв’язку, сортувальник поштових відправлень <br> та виробів друку</span></div>
	<div class="specialty-view-description">
		<p><strong>Оператор поштового зв’язку</strong> &ndash; найбільш поширена професія серед поштових працівників, вона є найбільш давня, так  як надання послуг поштового зв’язку існує ще з давнини. З кожним днем зростає кількість відділень поштового зв’язку оснащених комп’ютерною та касовою технікою, впровадженням додаткових фінансових послуг у зв’язку з чим виникає потреба в «Операторах поштового зв’язку, сортувальниках поштових відправлень та виробів друку», «Операторах поштового зв’язку, операторах комп’ютерного набору»</p>
		<p>Оператор поштового зв’язку здійснює прийом, обробку, сортування поштових відправлень, грошових переказів, зберігання і облік грошової готівки, виконує ведення виробничої документації, а також прийом і відправку пошти; контролює поповнення знаків поштової оплати, асортименту товарів в поштовому відділенні і їх збереження, справність і правильність експлуатації контрольно-касової машини; перевіряє кількість і якість поштових відправлень; підраховує вартість послуги, отримує гроші, видає чек та здачу клієнту; усуває дрібні несправності контрольно-касової машини; пропонує клієнтам додаткові,фінансові послуги які надає підприємство ПАТ «Укрпошта», «Нова пошта».</p>
		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
		<ul>
			<li><strong>1 рік</strong> &ndash; на базі повної загальної середньої освіти (11 класів)</li>
