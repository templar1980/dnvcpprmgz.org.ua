		<ul style="list-style:  circle ">
				<li>директивні документи, інструкції та інші  нормативні документи, які використовуються на даному робочому місці;</li>
				<li>адміністративно-територіальний поділ України; порядок приписки  поштових відправлень до документів;</li>
				<li>тарифи на послуги зв’язку і тарифи на інші послуги;</li>
				<li>порядок приймання передплати та переадресування періодичних видань;</li>
				<li>порядок ведення виробничої документації;</li>
				<li>інструкції з виплати пенсій і допомог;</li>
				<li>порядок ведення касових операцій підприємствами поштового зв’язку України;</li>
				<li>«Правила надання послуг поштового зв’язку»;</li>
				<li>порядок сортування, оброблення кореспонденції та періодичних видань;</li>
				<li>розклади руху поштового транспорту, плани направлення пошти та періодичних видань;</li>
				<li>контрольні строки проходження поштових відправлень та періодичних видань;</li>
				<li>список газетних вузлів України;</li>
				<li>керівництво по прийому, направленню міжнародних поштових відправлень;</li>
				<li>правила розповсюдження періодичних друкованих видань, правила перевезення пошти автомобільним транспортом;</li>
				<li>правила прийому та відправки  міжнародної пошти пунктами міжнародного поштового обміну;</li>
				<li>алфавітний покажчик районних центрів і міст обласного підпорядкування;</li>
				<li>правила роботи  з електронно-обчислювальною технікою;</li>
				<li>технології надання послуг з використанням автоматизованого робочого місця та іншого обладнання,правила його експлуатації;</li>
				<li>інструкцію про порядок обліку і збереження умовних цінностей;</li>
				<li>закони України «Про поштовий зв’язок», «Про захист прав споживачів»;</li>
				<li>основи електротехніки;</li>
				<li>основи галузевої економіки;</li>
				<li>основи правових знань;</li>
				<li>основи охорони праці.</li>
		</ul>
		
		<p><i class="fa fa-gear"></i><strong>Повинен вміти:</strong></p>
		<ul style="list-style:  circle ">
				<li>приймати, обробляти, відправляти та видавати внутрішню та міжнародну просту та рекомендовану кореспонденцію;</li>
				<li>обробляти мішки з письмовою кореспонденцією та періодичними виданнями;</li>
				<li>виписувати супровідні документи;</li>
				<li>здійснювати передплату на періодичні видання;</li>
				<li>видавати письмову кореспонденцію, адресовану «До запитання»;</li>
				<li>оформляти невручені поштові відправлення;</li>
				<li>реалізовувати знаки поштової оплати і т. ін.;</li>
				<li>контролювати  доставку кореспонденції та періодичних видань;</li>
				<li>складати сортувальні таблиці і вносити  зміни до  них;</li>
				<li>сортувати і  розкладати  поштові  відправлення  та  періодичні  видання по доставних дільницях та видавати їх листоношам;</li>
				<li>складати  поштові відправлення та періодичні видання  в абонентські скриньки, що розміщені у відділеннях зв’язку;</li>
				<li>переадресовувати періодичні видання.</li>
		</ul>		
