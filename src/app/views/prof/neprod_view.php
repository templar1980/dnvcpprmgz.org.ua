				<li>Обслуговувати покупців, показувати товари, демонструвати їх в дії, консультувати щодо конструктивних особливостей окремих видів товарів, їх призначення, властивостей, якості, правил догляду за ними, цін, стану моди поточного сезону;</li>
				<li>Здійснювати відмірювання, зважування, підрахунок вартості покупки, зчитувати сканером штрих-кодові позначки товару, проводити розрахунок покупців через реєстратори розрахункових операцій (РРО) (або перевіряти реквізити наданого чеку), видавати покупку;</li>
				<li>Готувати товар до продажу, розпаковувати, розглядати зовнішній вигляд виробу, комплектувати, перевіряти експлуатаційні властивості, підпрасовувати;</li>
				<li>Готувати робоче місце: перевіряти наявність та справність торгово-технологічного обладнання, інвентарю та інструментів, готувати до роботи РРО;</li>
				<li>Отримувати і готувати пакувальний матеріал;</li>
				<li>Розміщувати та викладати товари за групами, видами, сортами з урахуванням товарного сусідства, частоти попиту та зручності роботи;</li>
				<li>Забезпечувати товари цінниками;</li>
				<li>Оформляти вітрини прилавків;</li>
				<li>Підраховувати гроші (чеки) та здавати їх у встановленому порядку;</li>
				<li>Прибирати нереалізовані товари і тару;</li>
				<li>Готувати товари до інвентаризації.</li>
