	<div class="specialty-view-name"><span>Продавець непродовольчих товарів</span></div>
	<div class="specialty-view-description">
		<p><strong>Торгове ремесло </strong> &ndash; одне з найдавніших занять людини. Воно з'явилося тоді, колибули придумані перші гроші, і людям необхідно було обміняти товар на товарчи товар на гроші. Однак слово "продавець" прийшло в наш побут лише наприкінці XIX століття, коли стали з'являтися великі торгові підприємства й універмаги. Сучасний працівник торгівлі - це ініціативна, самостійна в прийнятті рішень особистість, яка відзначається творчим підходом до вирішення всіх питань, пов'язаних з торгівлею. З кожним роком зростає кількість магазинів оснащених комп’ютерною та касовою технікою, у зв’язку з чим виникає потреба в контролерах-касирах, продавцях.</p>
		<p><strong>Продавець</strong> &ndash; найбільш поширена професія серед торговельних працівників, вона й найбільш давня, так  як купівля-продаж існує дуже давно. Сучасні магазини різноманітні – це невеличкі магазини і магазини-супермаркети та гіпермаркети. Від організації робочого місця залежить дуже багато; швидкість обслуговування, настрій касира та покупця, збільшення продажу товару, багато іншого. Контролер-касир здійснює прийом, зберігання і облік грошової готівки, виконує ведення товарної документації, а також прийом і відправку товару; контролює поповнення асортименту товарів в торгівельному залі і їх збереження, справність і правильність експлуатації контрольно-касової машини; перевіряє кількість і якість товару; підраховує вартість покупки, отримує гроші, видає чек та здачу; усуває дрібні несправності контрольно-касової машини; консультує покупців про призначення, властивості, якість товарів і їх ціни; вивчає попит покупців; отримує товар зі складу; складає товарні звіти, акти.</p>
