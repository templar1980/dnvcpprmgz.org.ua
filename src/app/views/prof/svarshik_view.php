			<li>бути зібраним і уважним</li>
			<li>вестибулярний апарат повинен бути пристосований до роботи на висоті</li>
		</ul>
		<p><i class="fa fa-bullseye"></i><strong>Умови роботи:</strong> зварник ручного зварювання, як правило, виконує роботи як у приміщенні так і на відкритому просторі. Можливо виконання робіт на висоті і в незручних небезпечних положеннях. Велике навантаження приходиться на опорно-рухомий і зоровий апарати. Для захисту від теплових і світлових випромінювань використовують спеціальний одяг і маску (щиток) з захисним світофільтром. Режим роботи змінний. </p>
		
