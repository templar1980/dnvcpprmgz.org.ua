		<p>Основним знаряддям роботи електрозварника ручної зварки є електрод. У процесі роботи електрозварник виконує поступальні і коливальні переміщення електроду, регулює температуру, довжину дуги і процес утворення шва. Це одна з найбільш шанованих технічних спеціальностей. Якщо вилучити їхню роботу з тих місць, де вона зараз знаходиться, то наш світ розлетівся би по швах.</p>

