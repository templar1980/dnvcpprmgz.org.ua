		<p>Значимість електрики для сучасної людини переоцінити складно. Клацання вимикача став таким звичним і невід'ємним звуком, що уявити собі життя без цього «чарівного світу » просто неможливо. На сьогоднішній день електромонтери незамінні на різних підприємствах народного господарства. Затребувана їх майстерність і в побуті.</p>
		<p>З кожним роком електромонтери стають все більш затребуваними в сучасному суспільстві. Саме тому і ставлення до них міняється рік від року: зростають зарплати, забезпечуються додаткові можливості для відпочинку фахівців. Одним з прикладів людської пошани до праці електромонтера, стало відкриття пам'ятників у багатьох містах світу, що підтверджують складність та незамінність цієї професії у сучасному суспільстві.</p>

