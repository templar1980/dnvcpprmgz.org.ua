		<p>Перелік вимог, висунутих до майбутнього електромонтера, значний. Так як молодому спеціалісту належить працювати в небезпечних умовах, то, перш за все, увагу звертають на його фізичний стан. Витривалість, відмінний зір і відчуття рівноваги - ось неодмінні умови успішної роботи електромонтера. Крім цього, він повинен бути готовий приймати швидкі, обдумані рішення в разі виникнення позаштатної ситуації.</p>
	
