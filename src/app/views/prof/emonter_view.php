		<p>Eлектромонтер з ремонту та обслуговування електроустаткування обслуговує силові і освітлювальні установки, здійснює перевірку, розбирання і складання схем керування електроустаткуванням, підключення до електросистеми цеху нового обладнання, його перевірку, регулярний нагляд за його роботою. Він виявляє вузли, де виникло замикання, здійснює вимкнення цих вузлів із мережі, розбирання, заміну, складання і вмикання установки. Електромонтер здійснює діяльність як в приміщенні, так і поза ним.</p>

