	<div class="specialty-view-name"><span>Електромонтер з ремонту та обслуговування електроустаткування</span></div>
	
	<div class="specialty-view-description">
		<p><strong>Електрика</strong> &ndash; сукупність явищ, у яких виявляється існування, рух і взаємодія заряджених часток.</p>
		<p>Професія «<strong>Електромонтер»</strong> відноситься до типу «людина-техніка» та за характером праці є професією виконавського класу. Це професія підвищеного ризику. На сьогоднішній день електромонтери незамінні на різних підприємствах народного господарства. Затребувана їх майстерність і в побуті.</p>

		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
