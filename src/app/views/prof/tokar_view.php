		<p><i class="fa fa-check-square-o"></i><strong>Загально професійні вимоги:</strong></p>
		<ul style="list-style: circle">
			<li>раціонально та ефективно організовувати працю на робочому місці</li>
			<li>додержуватись норм технологічного процесу</li>
			<li>не допускати браку в роботі</li>
			<li>знати й виконувати вимоги нормативних актів про охорону праці й навколишнього середовища, додержуватись норм, методів і прийомів безпечного ведення робіт</li>
			<li>використовувати в разі необхідності засоби попередження і усунення природних і непередбачених негативних явищ (пожежі, аварії, повені тощо)</li>
			<li>знати інформаційні технології</li>
		</ul>
		<p><i class="fa fa-industry"></i><strong>Сфера професійного використання випускника:</strong> виробництво машин та устаткування, виробництво транспортного устаткування.</p>
		<p><i class="fa fa-user-circle"></i><strong>Специфічні вимоги:</strong> вік по закінченню терміну навчання &ndash; не менше 16 років</p>
		<p><i class="fa fa-medkit"></i><strong>Медичні обмеження:</strong> порушення координації рухів, виражені порушення зору, деформація пальців рук.</p>
		<p><i class="fa fa-grav"></i><strong>Вимоги до індивідуально-психологічних особливостей:</strong></p>
		<ul style="list-style: circle">
				<li>добре розвинене просторове уявлення</li>
				<li>точність і узгодженість рухів</li>
				<li>творче технічне мислення</li>
				<li>стійка увага</li>
		</ul>
		<p><i class="fa fa-bullseye"></i><strong>Умови роботи:</strong> робоче місце робітника &ndash; це цех чи майстерня, гарно освітлені й укомплектовані необхідними інструментами.</p>
		<p><i class="fa fa-exclamation-triangle"></i><strong>Шкідливі фактори виробництва:</strong> підвищений рівень шуму, блиск оброблюваного металу, напружена одноманітна поза. </p>
		<p><i class="fa fa-cogs"></i><strong>Ринок праці:</strong> заводи, підприємства по виготовленню металоконструкцій і сільськогосподарської техніки, деталей, верстатобудівні організації, автомобілебудування, суднобудування, фірми, що займаються будівельним дизайном та ремонтні майстерні. </p>
