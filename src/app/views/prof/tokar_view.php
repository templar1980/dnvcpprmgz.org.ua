	<div class="specialty-view-description">
		<p><strong>Токар</strong> – це фахівець, який займається механічною обробкою деталей з різноманітних матеріалів твердих порід (металу, пластмаси, дерева і т. д.) за допомогою володіння навичками роботи на токарному верстаті.</p>
		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
		<ul>
			<li><strong>1рік</strong> &ndash; на базі повної загальної середньої освіти (11класів)</li>
			<li><strong>4-6 місяців</strong> &ndash; професійно-технічне навчання (перепідготовка та підвищення кваліфікації) за рахунок фізичних та юридичних осіб</li>
		</ul>
		<div class="warning">
			<div><i class="fa fa-info-circle"></i></div>
