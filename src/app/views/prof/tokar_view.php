		<p><i class="fa fa-info-circle"></i><strong>Повинен знати:</strong> будову, правила підналагодження та перевірки на точність токарних верстатів; правила керування великогабаритними верстатами, які обслуговує разом з токарем більш високої кваліфікації; будову і правила застосування універсальних і спеціальних пристроїв; будову та умови застосування плазмотрона; призначення та правила застосування складного контрольно-вимірювального інструменту і приладів; геометрію і правила заточування різального інструменту, виготовленого з інструментальних сталей та з пластиною із твердих сплавів або керамічною; допуски і посадки, квалітети і параметри шорсткості; основні властивості матеріалів, які обробляє; електротехніку в межах кваліфікації. </p>
		<p><i class="fa fa-gear"></i><strong>Повинен вміти:</strong> обробляти на універсальних токарних верстатах деталі за 8-11-м квалітетами (3-4-м класами точності) та складні деталі за 12-14-м квалітетами (5-7-м класами точності). Обробляти деталі за 7-10-м квалітетами (2-3-м класами точності) на спеціалізованих верстатах, налагоджених для оброблення визначених деталей або для виконання окремих операцій. Виконувати токарне оброблення тонкостінних деталей з товщиною стінки до 1 мм та з довжиною до 200 мм. Виконувати токарні роботи методом суміщеного плазмово-механічного оброблення під керівництвом токаря більш високої кваліфікації.. Нарізати зовнішню і внутрішню однозахідну трикутну, прямокутну та трапецоїдну різьбу різцем. Нарізати різьби вихровими головками. Керувати токарно-центровими верстатами з висотою центрів 2000 м та вище, з відстанню між центрами 10000 мм і більше. Керувати токарно-центровими верстатами з висотою центрів до 800 мм, які мають більше трьох супортів під керівництвом токаря більш високої кваліфікації. Виконувати необхідні розрахунки для одержання заданих конусних поверхонь. Керувати піднімально-транспортним устаткуванням з підлоги. Стропувати та ув’язувати вантажі для піднімання, переміщення, установлення та складування. Виконувати токарне оброблення заготовок зі слюди та мікалексу.</p>
