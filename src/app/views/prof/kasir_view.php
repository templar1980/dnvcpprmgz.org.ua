	<div class="specialty-view-description">
		<p><strong>Контролер-касир</strong> здійснює прийом, зберігання і облік грошової готівки, виконує ведення товарної документації, а також прийом і відправку товару; контролює поповнення асортименту товарів в торгівельному залі і їх збереження, справність і правильність експлуатації контрольно-касової машини; перевіряє кількість і якість товару; підраховує вартість покупки, отримує гроші, видає чек та здачу; усуває дрібні несправності контрольно-касової машини; консультує покупців про призначення, властивості, якість товарів і їх ціни; вивчає попит покупців; отримує товар зі складу; складає товарні звіти, акти.</p>
