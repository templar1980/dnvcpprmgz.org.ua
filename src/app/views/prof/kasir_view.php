				<li>порядок підготовки робочого місця контролера касира до роботи;</li>
				<li>порядок підготовки касового апарата до роботи;</li>
				<li>правила експлуатації сучасних видів реєстраторів розрахункових операцій (РРО);</li>
				<li>форми розрахунку із споживачами, в тому числі за кредитними картками; </li>
				<li>асортимент товарів;</li>
				<li>правила розшифрування артикулів, штрихового коду товару;</li>
				<li>порядок програмування товарів на різних моделях реєстраторів розрахункових операцій (РРО), електронних контрольно-касових апаратів (ЕККА);</li>
				<li>порядок  роботи в системі єдиного вузла розрахунку;</li>
				<li>порядок виконання заключних операцій на реєстраторах розрахункових операцій (РРО);</li>
				<li>правила оформлення книги обліку розрахункових операцій та складання  касової звітності; </li>
				<li>технічні засоби штрихового кодування та їх характеристики; </li>
				<li>порядок та умови маркування товарів внутрішніми штриховими кодами;  </li>
				<li>правила  і  норми  охорони  праці, протипожежного захисту, виробничої санітарії та особистої гігієни.</li>
		</ul>
		
		<p><i class="fa fa-gear"></i><strong>Повинен вміти:</strong></p>
		<ul style="list-style:  circle ">
				<li>організувати робоче місце;</li>
				<li>повідомляти  адміністрацію  про надходження товарів, які не відповідають  маркуванню; </li>
				<li>проводити готівкові  та безготівкові розрахунки із споживачами  в залежності від форми обслуговування; </li>
				<li>обслуговувати покупців із застосуванням технічних засобів  штрихового  кодування; </li>
				<li>перевіряти   наявність штрихового коду, інших штрихкодових позначок на товарах; </li>
				<li>проводити ідентифікацію товарів; </li>
				<li>надавати  покупцям допомогу  в  пакуванні  товарів; </li>
				<li>отримувати денний та фіскальний звітні чеки,  обнулювати оперативну пам’ять реєстраторів розрахункових операцій (РРО), електронних-контрольно-касових апаратів (ЕККА);</li> 
				<li>готувати грошові кошти до здачі в головну касу магазину;</li>
				<li>виконувати інкасацію на реєстраторах розрахункових операцій (РРО);</li>
				<li>вести книгу обліку розрахункових операцій, складати касову звітність; </li>
				<li>брати участь у підготовці товарів до інвентаризації.</li>
		</ul>		
