	<div class="specialty-view-description">
		<p><strong>Водій</strong> – особа, яка керує транспортним засобом і має відповідне посвідчення встановленого зразка.</p>
		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
		<ul>
			<li><strong>3 роки</strong> &ndash; на базі базової загальної середньої освіти  (9 класів) в інтеграції з професією: <a href="/specialty/avtoslesar">Слюсар з ремонту автомобілів</a> або <a href="/specialty/svarshik">Електрогазозварник</a></li>
			<li><strong>3-5 місяців</strong> &ndash; професійно-технічне навчання (перепідготовка та підвищення кваліфікації) за рахунок фізичних або юридичних осіб</li>
		</ul>
