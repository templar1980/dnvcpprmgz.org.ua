		<p>Водій транспортного засобу — одна з численних професій, попит на яку постійно зростає. Водій повинен не тільки додержуватися правил дорожного руху, а й досконало володіти прийомами керування транспортним засобом. Чим глибше він знає сутність явищ, які відбуваються при керуванні, тим скоріше стане кваліфікованим фахівцем.</p>

