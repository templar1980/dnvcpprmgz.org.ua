		<p>Дизайн інтер'єру &ndash; це творче творення, вдале виконання якого вимагає чималого досвіду, відточеної художнього смаку, постійного самовдосконалення і титанічної копіткої праці.</p>
		<p>Дизайн &ndash; (від англійського design &ndash; проектувати, креслити, задумати, а також проект, план, малюнок) термін, що позначає різновид художньо-проектної діяльності, що охоплює створення промислових виробів і раціональне формування цілісної предметного середовища. У вузькому сенсі &ndash; художнє конструювання.</p>
		<p>Інтерьєр &ndash; (від французького interieur &ndash; внутрішній) 1. В архітектурі &ndash; внутрішній простір будівлі чи якесь приміщення. 2. В живопису &ndash; жанр, що досяг розквіту в XVII столітті в голландського й фламандського живопису, а також у XIX столітті в Росії (венеціановская школа).</p>
		<p>У різний час люди воліють певний декоративний стиль. Знання історичної епохи, знайомство з роботами художників-оформлювачів сприяє правильному вибору.</p>
		<p>Дизайнерський стиль деяких виробів здається потворним, перевантаженим деталями, безглуздим за пропорціями або непрактичним; стиль інших приваблює красою; є предмети, що радують око і вражають своєю безглуздістю. Яскравий афоризм Вільяма Морріса: «Не тримайте в будинку нічого, що не вважаєте красивим або не знаєте його призначення» &ndash; грішить суб'єктивізмом. Кожен читач знайде улюблений період або стиль. Показуючи, як стилі певного періоду входять в ужиток, ми намагаємося дати найбільш точні та наочні зразки.</p>
		<p>Гарне знання деталей, форми і малюнка сприяє розширенню кругозору, незалежно від того, яким ремеслом Ви займаєтеся або захоплюєтеся. Подібне виклад дозволяє зосередитися на стилях різних країн, груп і технік.</p>

