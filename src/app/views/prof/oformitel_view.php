	<div class="specialty-view-name"><span>Оформлювач  вітрин, приміщень та будівель</span></div>
	<div class="specialty-view-description">
		<p><strong>Оформлювач  вітрин, приміщень та будівель </strong> &ndash; фахівець, який виконує роботи з художнього та художньо-декоративного оформлення вітрин, приміщень, будівель, виготовлення деталей і фрагментів інтер'єру, панно, стендів, декоративних споруд тощо та встановлює їх. Використовує художні методи для створення необхідних ефектів під час оформлення вітрин та інших місць для виставлення товарів, рекламних засобів тощо. Здійснює комплексне рекламне оформлення вітрин, інтер'єрів приміщень, а також рекламних плакатів, вивісок, щитів, стендів, настінних панно тощо.</p>

