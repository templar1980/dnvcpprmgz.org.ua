		<p>Верстатник працює в закритому приміщенні, при хорошому освітленні, нормальній температурі повітря. Робота верстатника двозмінна, режим вільний. Тривалість робочого дня – 8 годин, для неповнолітніх – 6.  Робота верстатника кваліфікується як праця середньої важкості. Вона добре механізована, виконувати її можуть як чоловіки, так і жінки. Працює верстатник, в основному, стоячи. </p>
		<p>Заробітна плата верстатника залежить від кваліфікації і складності виконуваної роботи. На підприємствах, крім цього, діє система матеріального заохочення: премії за виконання виробничих планів, за хорошу якість продукції, за раціоналізацію і винахідництво та інші форми. </p>

