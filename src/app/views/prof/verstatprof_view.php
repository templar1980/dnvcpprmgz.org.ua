	<div class="specialty-view-name"><span>Верстатник широкого профілю</span></div>
	<div class="specialty-view-description">
		<p><strong>Верстатник широкого профілю</strong> працює на металообробному виробництві, в ремонтних майстернях, ремонтних цехах різних виробництв. Спеціальність виникла у зв'язку з необхідністю оперативного виготовлення невеликих партій деталей різної складності для ремонту верстатів і механізмів. Верстатник широкого профілю обробляє деталі з металу і інших матеріалів на токарних, фрезерних, свердлувальних і шліфувальних верстатах.</p>
		<p>Керуючись кресленням деталі, визначає послідовність її виготовлення. Підбирає необхідні для цього інструменти. Використовує довідники і проводить необхідні розрахунки. Вибирає режим різання, проводить наладку верстата, встановлює інструмент і заготівку, проводить обробку деталі. Вивіряє за допомогою оптичних приладів розміри деталі і якість її обробки. Використовує ручні інструменти, механічне устаткування (токарні, свердлувальні, фрезерні, шліфувальні верстати) і вимірювальні прилади. </p>
		<p><i class="fa fa-hourglass-end"></i><strong>Термін навчання:</strong></p>
		<ul>
			<li><strong>1рік</strong> &ndash; на базі повної загальної середньої освіти (11класів)</li>
			<li><strong>3 роки</strong> &ndash; на базі базової загальної середньої освіти  (9 класів) в інтеграції з професією: <a href="/specialty/progoperator"><strong>Оператор верстатів з програмним керуванням</strong></a></li>
			<li><strong>4-6 місяців</strong> &ndash; професійно-технічне навчання (перепідготовка та підвищення кваліфікації) за рахунок фізичних або юридичних осіб</li>
		</ul>
		<div class="warning">
			<div><i class="fa fa-info-circle"></i></div>
