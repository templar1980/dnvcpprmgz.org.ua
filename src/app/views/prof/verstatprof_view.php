		<p><i class="fa fa-gear"></i><strong>Повинен вміти:</strong></p>
		<ul style="list-style:  circle ">
				<li>обробляти деталі на токарних та фрезерних верстатах за 7-10-м квалітетами точності, на свердлувальних верстатах за 6-9-м квалітетами точності та на шліфувальних верстатах із застосуванням охолоджувальної рідини за 7-8-м квалітетами точності із застосуванням різних різальних інструментів та універсальних пристроїв</li>
				<li>нарізати різьби діаметром понад 42мм на свердлильних верстатах</li>
				<li>нарізати двозахідну зовнішню і внутрішню різьбу, різьбу трикутного, прямокутного, напівкруглого профілю, упорні та трапецоїдні різьби на токарних верстатах</li>
				<li>фрезерувати відкриті та напіввідкриті поверхні різних конфігурацій та сполучень, різьби, спіралі, зубці зубчастих коліс та рейок</li>
				<li>шліфувати і нарізати рифлення на поверхні бочки валків на шліфувально-рифельних верстатах</li>
				<li>установлювати великі деталі складної конфігурації, які вимагають комбінованого кріплення та точного вивіряння в різних площинах</li>
				<li>налагоджувати верстати, які обслуговує</li>
				<li>визначати послідовність переходів та виконувати необхідні технічні розрахунки, що пов’язані з обробкою деталей</li>
				<li>читати робочі креслення деталей</li>
				<li>користуватись технологічною документацією та паспортами верстатів</li>
		</ul>
