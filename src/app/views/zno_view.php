	<h2>Тести ЗНО онлайн</h2>
	<p>Сайт для проходження тестів ЗНО онлайн та підготовки майбутніх абітурієнтів для проходження зовнішнього незалежного оцінювання у 2018 році створений провідним українським освітнім інтернет-ресурсом Освіта.ua.</p>
	<p>На сайті «ЗНО онлайн» розміщені тести, що складали абітурієнти під час зовнішнього незалежного оцінювання 2009-2017 років, а також варіанти тестів, що пропонувались вступникам до вищих навчальних закладів України під час пробного зовнішнього незалежного оцінювання 2009-2017 років.</p>
