    <h1><i class="fa fa-unlock-alt"></i>Відкриті ресурси</h1>
    <img src="/img/pages/_content/openinf_main.jpg" alt="" style="width: 100%; margin-bottom: 20px;">
    <p>Свою діяльність <strong>ДПТНЗ «Дніпровський центр професійно-технічної освіти»</strong> регламентує наказами та іншими нормативними документами затвердженими Міністерством освіти і науки України. Базуючись на закони України, а також на Положення, дія яких поширюється на професійно-технічні освітні установи, створюються правила, накази, акти та інші документи, що складають правову базу центру.</p>
    <p>За статтею 30 Закону України «Про освіту» від 05.09.2017 № 2145-VIII Дніпровський центр професійно-технічної освіти пропонує відкриті ресурси з інформацією про свою діяльність.</p>
